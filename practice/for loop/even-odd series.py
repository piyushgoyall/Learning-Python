# Take the starting and ending values as input and print all odd and even numbers between the entered range.

s = int(input())
e = int(input())
print()
print("Even numbers : ",end=" ")
for i in range(s,e+1,1):
    if i%2==0:
        print(i,end="\t")
print()
print()
print("Odd numbers :  ",end=" ")
for i in range(s,e+1,1):
    if i%2==1:
        print(i,end="\t")
